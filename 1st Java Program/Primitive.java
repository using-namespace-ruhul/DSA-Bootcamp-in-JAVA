package com.company;

public class Primitive {
    //Primitive data type means any data type which can not be broken even further.
    //e.g- int,float......but string isn't a primitive data type since it can be broken into chars.
    public static void main(String[] args) {
        int rollno = 64;
        char letter = 'r';
        float marks = 98.77f;
        double large_Decimal_Nos = 4345.642358;
        long large_Integer = 123545654696L;
        boolean check = false;
        String name = "Ruhul Amin Sardar";
        System.out.println(rollno);
        System.out.println(letter);
        System.out.println(large_Decimal_Nos);
        System.out.println(check);
        System.out.println(name);
    }
}
